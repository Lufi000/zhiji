    /// 获取成长视角心法列表（按能量配置排序：先推荐与该类型匹配的心法，再补其余）
    static func getGrowthFrameworks(orderedByStrength strength: String) -> [GrowthFrameworkBlock] {
        let order = growthFrameworkOrderByStrength[strength] ?? growthFrameworkOrderByStrength["均衡型"] ?? []
        var byTitle: [String: GrowthFrameworkBlock] = [:]
        for block in growthFrameworks {
            byTitle[block.title] = block
        }
        var result: [GrowthFrameworkBlock] = []
        var used = Set<String>()
        for title in order {
            if let block = byTitle[title] {
                result.append(block)
                used.insert(block.title)
            }
        }
        for block in growthFrameworks where !used.contains(block.title) {
            result.append(block)
        }
        return result
    }
